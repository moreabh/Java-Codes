class StatVar4
{
	static String s;
	public static void main(String[]args)
	{
		System.out.println(s);

		demo();

	String s = "Abhishek";
	System.out.println(s);
	System.out.println(StatVar4.s);
		
	}
	public static void demo()
	{
		s = "Qspider";
		System.out.println(s);
	}
}
