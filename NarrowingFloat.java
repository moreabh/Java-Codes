class NarrowingFloat
{
	public static void main( String[]args)
	{
		
		float b=(float)35.46f;
		System.out.println(b);
		long c=(long)b;
		System.out.println(c);
		int d=(int)b;
		System.out.println(d);
		char g=(char)b;
		System.out.println(g);
		short e=(short)b;
		System.out.println(e);
		byte f=(byte)b;
		System.out.println(f);
		

	}

}